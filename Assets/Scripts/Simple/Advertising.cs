    // Update is called once per frame
    void Update()
    {
        if (adv)
        {
            helpButton.SetActive(true);
            adv = false;
        }
    }

    public void Adv()
    {
        timer = 0;
        StartCoroutine(Advt());
    }

    IEnumerator Advt()
    {
        while (timer < 1)
        {
            timer += Time.deltaTime;
            yield return null;
        }
        adv = true;
    }

    public void Help()
    {
        num = Inventar.inventar.HelpNeed();
